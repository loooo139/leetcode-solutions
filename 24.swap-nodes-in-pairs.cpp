/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    ListNode* swapPairs(ListNode* head) {
        if(!head||!head->next)
            return head;
        ListNode *tmp=swapPairs(head->next->next);
        ListNode *next=head->next;
        next->next=head;
        head->next=tmp;
        return next;
    }
};

